"""Какую модель под какую задачу: классификатор сложности — Build It для урока 9.2.

Без зависимостей. Оценивает сложность запроса по признакам (длина, ключевые слова,
нужно ли рассуждение/код) и маршрутизирует: простое → дешёвая модель, сложное →
топ. Правильная модель под задачу, а не одна на всё.
"""

# Триггеры повышенной сложности (стемы — подстроки, учитывают морфологию).
HARD_HINTS = ("код", "програм", "докаж", "проанализир", "анализ", "оптимизир",
              "архитектур", "рассужд", "стратег")
EASY_HINTS = ("привет", "переведи", "классифиц", "извлеки", "список")

ROUTES = {"low": "gemini-flash", "medium": "sonnet-4.6", "high": "opus-4.8"}


def classify(request):
    """Вернуть сложность запроса: 'low' | 'medium' | 'high'."""
    text = request.lower()
    if any(h in text for h in HARD_HINTS) or len(request) > 600:
        return "high"
    if any(h in text for h in EASY_HINTS) and len(request) < 200:
        return "low"
    return "medium"


def route(request):
    """Подобрать модель под сложность запроса."""
    return ROUTES[classify(request)]


if __name__ == "__main__":
    samples = [
        "Переведи фразу на английский",
        "Сделай саммари встречи в 5 пунктах",
        "Проанализируй архитектуру и предложи оптимизацию кода",
    ]
    for s in samples:
        print(f"{classify(s):<7} → {route(s):<13} | {s}")
