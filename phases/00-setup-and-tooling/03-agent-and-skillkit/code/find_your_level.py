def _validate_choices(answers, size: int, option_count: int = 4) -> list[int]:
    answers = list(answers)
    if len(answers) != size:
        raise ValueError(f"нужно ответов: {size}, получено: {len(answers)}")
    if any(not isinstance(choice, int) or not 0 <= choice < option_count for choice in answers):
        raise ValueError(f"каждый ответ должен быть целым индексом от 0 до {option_count - 1}")
    return answers


def self_profile(answers) -> dict[int, int]:
    """Возвращает самооценку 0..3 отдельно по каждой фазе 0–11."""
    answers = _validate_choices(answers, len(SELF_ASSESSMENT))
    return {
        item["phase"]: item["options"][choice][1]
        for item, choice in zip(SELF_ASSESSMENT, answers)
    }


