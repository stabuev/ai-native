PASS_THRESHOLD = 0.8
LESSON_THRESHOLD = 0.5


def _validate_answers(answers, bank) -> list[int]:
    answers = list(answers)
    if len(answers) != len(bank):
        raise ValueError(f"нужно ответов: {len(bank)}, получено: {len(answers)}")
    for choice, item in zip(answers, bank):
        if not isinstance(choice, int) or not 0 <= choice < len(item["options"]):
            raise ValueError("каждый ответ должен быть целым индексом существующего варианта")
    return answers


def grade(answers, bank) -> dict:
    """Оценивает фазу по общему баллу и минимальному покрытию каждого урока."""
    if not bank:
        raise ValueError("банк вопросов не может быть пустым")
    answers = _validate_answers(answers, bank)

    lesson_stats: dict[str, dict[str, int]] = {}
    competency_stats: dict[str, dict[str, int]] = {}
    feedback = []
    for choice, item in zip(answers, bank):
        is_correct = choice == item["correct"]
        lesson = lesson_stats.setdefault(item["lesson"], {"correct": 0, "total": 0})
        lesson["total"] += 1
        lesson["correct"] += int(is_correct)
        competency = competency_stats.setdefault(item["competency"], {"correct": 0, "total": 0})
        competency["total"] += 1
        competency["correct"] += int(is_correct)
        if not is_correct:
            feedback.append({
                "question_id": item["id"],
                "lesson": item["lesson"],
                "competency": item["competency"],
                "selected": item["options"][choice],
                "correct_answer": item["options"][item["correct"]],
                "diagnosis": item["misconception"],
                "explanation": item["explanation"],
            })

    correct = sum(stats["correct"] for stats in lesson_stats.values())
    total = len(bank)
    score = correct / total
    by_lesson = {
        lesson: {
            **stats,
            "score": round(stats["correct"] / stats["total"], 2),
        }
        for lesson, stats in lesson_stats.items()
    }
    competency_scores = {
        competency: round(stats["correct"] / stats["total"], 2)
        for competency, stats in competency_stats.items()
    }
    coverage_ok = all(stats["score"] >= LESSON_THRESHOLD for stats in by_lesson.values())
    passed = score >= PASS_THRESHOLD and coverage_ok
    review_lessons = [lesson for lesson, stats in by_lesson.items() if stats["score"] < 1.0]
    retry_competencies = [name for name, value in competency_scores.items() if value < 1.0]

    return {
        "correct": correct,
        "total": total,
        "score": round(score, 2),
        "coverage_ok": coverage_ok,
        "verdict": "освоено" if passed else "нужна практика",
        "by_lesson": by_lesson,
        "competency_scores": competency_scores,
        "review_lessons": review_lessons,
        "feedback": feedback,
        "retry_tasks": [
            {"competency": name, **RETRY_TASKS[name]}
            for name in retry_competencies
        ],
        "assessment_note": (
            "Квиз показывает применение механизма в нескольких сценариях, но не заменяет "
            "артефакт, практику фазы или более широкую оценку навыка."
        ),
    }


def compare_with_baseline(result: dict, baseline: dict[str, float]) -> list[dict]:
    """Сравнивает форму B с сохранёнными баллами компетенций формы A."""
    progress = []
    for competency, exit_score in result["competency_scores"].items():
        if competency not in baseline:
            continue
        baseline_score = baseline[competency]
        if not isinstance(baseline_score, (int, float)) or not 0 <= baseline_score <= 1:
            raise ValueError("входной балл компетенции должен быть числом от 0 до 1")
        progress.append({
            "competency": competency,
            "baseline": round(float(baseline_score), 2),
            "exit": exit_score,
            "delta": round(exit_score - float(baseline_score), 2),
        })
    return progress


def run_quiz(phase: int, answers, baseline: dict[str, float] | None = None) -> dict:
    if phase not in BANKS:
        raise KeyError(f"нет выходного банка для фазы {phase}")
    result = grade(answers, BANKS[phase])
    result["phase"] = phase
    if baseline is not None:
        result["progress"] = compare_with_baseline(result, baseline)
        result["progress_note"] = (
            "Изменение по компетенции — ориентир между малым числом разных сценариев, "
            "а не точное измерение прироста навыка."
        )
    return result


def _parse_answers(raw: str) -> list[int]:
    try:
        answers = [int(value.strip()) for value in raw.split(",") if value.strip()]
    except ValueError as exc:
        raise ValueError("ответы должны быть индексами через запятую, например 2,0,3,1") from exc
    if not answers:
        raise ValueError("список ответов не может быть пустым")
    return answers


def main(argv=None) -> int:
    """CLI для запуска движка по абсолютному пути из любого рабочего каталога."""
    import argparse
    import json

    parser = argparse.ArgumentParser(description="Выходная сценарная проверка AI Native")
    parser.add_argument("--phase", type=int, required=True, choices=sorted(BANKS))
    parser.add_argument(
        "--answers",
        required=True,
        help="индексы ответов через запятую в исходном порядке",
    )
    parser.add_argument(
        "--baseline-json",
        help="необязательный JSON-объект baseline_competencies из персонального маршрута",
    )
    args = parser.parse_args(argv)

    try:
        answers = _parse_answers(args.answers)
        baseline = json.loads(args.baseline_json) if args.baseline_json else None
        if baseline is not None and not isinstance(baseline, dict):
            raise ValueError("baseline-json должен содержать JSON-объект")
        result = run_quiz(args.phase, answers, baseline=baseline)
    except (ValueError, KeyError, json.JSONDecodeError) as exc:
        parser.error(str(exc))

    print(json.dumps(result, ensure_ascii=False, indent=2))
    return 0


